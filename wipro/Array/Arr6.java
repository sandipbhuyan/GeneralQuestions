import java.util.Scanner;

class Arr6
{
    public static void main(String[] args) {
        int arr[] = {1,3,55,2,44,5,77};
        int n = arr.length; 
        for (int i = 1; i < n; ++i) { 
            int key = arr[i]; 
            int j = i - 1; 
            while (j >= 0 && arr[j] > key) { 
                arr[j + 1] = arr[j]; 
                j = j - 1; 
            } 
            arr[j + 1] = key; 
        } 
    }
}